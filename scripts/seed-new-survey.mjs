async function seed() {
  console.log("Cleaning up old data...");
  await supabase.from('response_answers').delete().neq('id', '00000000-0000-0000-0000-000000000000');
  await supabase.from('survey_responses').delete().neq('id', '00000000-0000-0000-0000-000000000000');
  await supabase.from('survey_sections').delete().neq('id', '00000000-0000-0000-0000-000000000000');
  await supabase.from('question_choices').delete().neq('id', '00000000-0000-0000-0000-000000000000');
  await supabase.from('questions').delete().neq('id', '00000000-0000-0000-0000-000000000000');
  await supabase.from('sections').delete().neq('id', '00000000-0000-0000-0000-000000000000');
  await supabase.from('surveys').delete().neq('id', '00000000-0000-0000-0000-000000000000');

  console.log("Seeding new sections and questions...");
  const sectionIds = [];
  
  for (let i = 0; i < surveyData.length; i++) {
    const cat = surveyData[i];
    const { data: section } = await supabase.from('sections').insert({
      code: `CAT${i + 1}`,
      title_en: cat.category,
      title_th: cat.category,
      sort_order: i + 1
    }).select().single();
    
    sectionIds.push(section.id);

    for (let j = 0; j < cat.questions.length; j++) {
      const q = cat.questions[j];
      const { data: question } = await supabase.from('questions').insert({
        section_id: section.id,
        type: 'single_select',
        text_en: q.q,
        text_th: q.q,
        required: true,
        sort_order: j + 1
      }).select().single();

      const scoreMap = [5, 4, 2, 1];
      const choiceInserts = q.choices.map((c, index) => ({
        question_id: question.id,
        value: `${scoreMap[index]}`, 
        label_en: c,
        label_th: c,
        sort_order: index + 1
      }));
      await supabase.from('question_choices').insert(choiceInserts);
    }
  }

  console.log("Creating new active survey...");
  const { data: survey } = await supabase.from('surveys').insert({
    title_en: "Annual Engagement Survey 2026",
    title_th: "แบบสำรวจความผูกพันองค์กรประจำปี 2026",
    status: 'active',
    survey_type: 'anonymous',
    start_date: new Date().toISOString(),
    end_date: new Date(Date.now() + 30 * 24 * 60 * 60 * 1000).toISOString(),
    target_responses: 500
  }).select().single();

  const surveySectionInserts = sectionIds.map((id, index) => ({
    survey_id: survey.id,
    section_id: id,
    sort_order: index + 1
  }));
  await supabase.from('survey_sections').insert(surveySectionInserts);

  console.log("Generating 100 random responses...");
  const { data: allQuestions } = await supabase.from('questions').select('id');
  const depts = ["IT", "Human Resources", "Sales", "Marketing", "Operations"];
  
  for (let i = 0; i < 100; i++) {
    const { data: response, error: respErr } = await supabase.from('survey_responses').insert({
      survey_id: survey.id,
      status: 'completed',
      anonymous_token: `anon-${i}`,
      demographics: {
        department: depts[Math.floor(Math.random() * depts.length)],
        location: "Head Office"
      },
      completed_at: new Date(Date.now() - Math.random() * 10 * 24 * 60 * 60 * 1000).toISOString()
    }).select().single();

    if (respErr || !response) {
      console.error("Error creating response:", respErr);
      continue;
    }

    const answers = allQuestions.map(q => {
      // Skew towards positive (5 or 4)
      const val = Math.random() > 0.3 ? (Math.random() > 0.5 ? '5' : '4') : (Math.random() > 0.5 ? '2' : '1');
      return {
        response_id: response.id,
        question_id: q.id,
        text_value: val,
        numeric_value: parseInt(val, 10)
      };
    });
    
    // Batch insert answers in chunks of 50
    for (let k = 0; k < answers.length; k += 50) {
      const { error: ansErr } = await supabase.from('response_answers').insert(answers.slice(k, k + 50));
      if (ansErr) console.error("Error inserting answers:", ansErr);
    }
  }

  console.log("✅ Seeding complete!");
}

seed().catch(console.error);
